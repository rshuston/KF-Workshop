%
% Inversion of symmetric 4x4 matrix
%

function inv_A = inv_sym_4x4(A)
    
    % A =
    %    a11 a12 a13 a14
    %    a12 a22 a23 a24
    %    a13 a23 a33 a34
    %    a14 a24 a34 a44
    
    a11 = A(1,1);
    a12 = A(1,2);
    a13 = A(1,3);
    a14 = A(1,4);
    
    a22 = A(2,2);
    a23 = A(2,3);
    a24 = A(2,4);
    
    a33 = A(3,3);
    a34 = A(3,4);
    
    a44 = A(4,4);
    
    % Minors
    
    m11 = a22 * (a33 * a44 - a34 * a34) ...
        - a23 * (a23 * a44 - a34 * a24) ...
        + a24 * (a23 * a34 - a33 * a24);
    m12 = a12 * (a33 * a44 - a34 * a34) ...
        - a23 * (a13 * a44 - a34 * a14) ...
        + a24 * (a13 * a34 - a33 * a14);
    m13 = a12 * (a23 * a44 - a34 * a24) ...
        - a22 * (a13 * a44 - a34 * a14) ...
        + a24 * (a13 * a24 - a23 * a14);
    m14 = a12 * (a23 * a34 - a33 * a24) ...
        - a22 * (a13 * a34 - a33 * a14) ...
        + a23 * (a13 * a24 - a23 * a14);
    
    m22 = a11 * (a33 * a44 - a34 * a34) ...
        - a13 * (a13 * a44 - a34 * a14) ...
        + a14 * (a13 * a34 - a33 * a14);
    m23 = a11 * (a23 * a44 - a34 * a24) ...
        - a12 * (a13 * a44 - a34 * a14) ...
        + a14 * (a13 * a24 - a23 * a14);
    m24 = a11 * (a23 * a34 - a33 * a24) ...
        - a12 * (a13 * a34 - a33 * a14) ...
        + a13 * (a13 * a24 - a23 * a14);
    
    m33 = a11 * (a22 * a44 - a24 * a24) ...
        - a12 * (a12 * a44 - a24 * a14) ...
        + a14 * (a12 * a24 - a22 * a14);
    m34 = a11 * (a22 * a34 - a23 * a24) ...
        - a12 * (a12 * a34 - a23 * a14) ...
        + a13 * (a12 * a24 - a22 * a14);
    
    m44 = a11 * (a22 * a33 - a23 * a23) ...
        - a12 * (a12 * a33 - a23 * a13) ...
        + a13 * (a12 * a23 - a22 * a13);
    
    % Cofactors
    
    c11 = m11;
    c12 = -m12;
    c13 = m13;
    c14 = -m14;
    
    c22 = m22;
    c23 = -m23;
    c24 = m24;
    
    c33 = m33;
    c34 = -m34;
    
    c44 = m44;
    
    % Determinant
    
    D = a11 * c11 + a12 * c12 + a13 * c13 + a14 * c14;
    
    % Adjoint scaled by 1/D
    
    inv_A = (1 / D) * [ c11 c12 c13 c14 ;
                        c12 c22 c23 c24 ;
                        c13 c23 c33 c34 ;
                        c14 c24 c34 c44 ];
    
end
