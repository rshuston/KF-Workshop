%
% Determinant of 4x4 matrix ... direct computation
%

function det_A = det_4x4(A)
    
    % A =
    %    a11 a12 a13 a14
    %    a21 a22 a23 a24
    %    a31 a32 a33 a34
    %    a41 a42 a43 a44
    
    m11 = A(2,2) * (A(3,3) * A(4,4) - A(3,4) * A(4,3)) ...
        - A(2,3) * (A(3,2) * A(4,4) - A(3,4) * A(4,2)) ...
        + A(2,4) * (A(3,2) * A(4,3) - A(3,3) * A(4,2));
    m12 = A(2,1) * (A(3,3) * A(4,4) - A(3,4) * A(4,3)) ...
        - A(2,3) * (A(3,1) * A(4,4) - A(3,4) * A(4,1)) ...
        + A(2,4) * (A(3,1) * A(4,3) - A(3,3) * A(4,1));
    m13 = A(2,1) * (A(3,2) * A(4,4) - A(3,4) * A(4,2)) ...
        - A(2,2) * (A(3,1) * A(4,4) - A(3,4) * A(4,1)) ...
        + A(2,4) * (A(3,1) * A(4,2) - A(3,2) * A(4,1));
    m14 = A(2,1) * (A(3,2) * A(4,3) - A(3,3) * A(4,2)) ...
        - A(2,2) * (A(3,1) * A(4,3) - A(3,3) * A(4,1)) ...
        + A(2,3) * (A(3,1) * A(4,2) - A(3,2) * A(4,1));
    
    det_A = A(1,1) * m11 - A(1,2) * m12 + A(1,3) * m13 - A(1,4) * m14;
    
end
